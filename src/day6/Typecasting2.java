package day6;

public class Typecasting2 {
	
	public static void main(String[] args) {
		 double sellerSalePrice=9.75;		//camel case
		
		 int newsPaperSalePrice=(int)sellerSalePrice;
		
		System.out.println("Price Mentioned By Seller "+sellerSalePrice);
		System.out.println("Price Mentioned By News Paper "+newsPaperSalePrice);
		
	}
	//datatype conversion is aka typecastin
}
